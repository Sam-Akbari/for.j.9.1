def fibonaci(n):#تابع فیبوناچی
    a, b = 0, 1#اول ا و ب صفر و یک به ترتیب هستند
    while a < n:#حالا اگر ا کوچک تر از ان باشد
        print(a, end=" ")#ا را چاپ میکند و اخر یک فاصله میگذارد
        a, b = b, a + b#ا و ب برابر با ب و جمع ا و ب است

num = int(input("Enter your number: "))#ععدی که کاربر وارد میکند
print(num, ":")#ععد کاربر را با یک : چاپ میکند
fibonaci(num)#پارامتر و نام تابع

